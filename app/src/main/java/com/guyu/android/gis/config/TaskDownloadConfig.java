package com.guyu.android.gis.config;

public class TaskDownloadConfig {
private String caseType;
private String attrsViewId;
private String geoViewId;

public String getCaseType() {
	return caseType;
}
public void setCaseType(String caseType) {
	this.caseType = caseType;
}
public String getAttrsViewId() {
	return attrsViewId;
}
public void setAttrsViewId(String attrsViewId) {
	this.attrsViewId = attrsViewId;
}
public String getGeoViewId() {
	return geoViewId;
}
public void setGeoViewId(String geoViewId) {
	this.geoViewId = geoViewId;
}

}
