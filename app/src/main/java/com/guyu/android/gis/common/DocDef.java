package com.guyu.android.gis.common;

public class DocDef {
private String docDefID;
private String bizID;
private String docDefName;
private int dispOrder;
public String getDocDefID() {
	return docDefID;
}
public void setDocDefID(String docDefID) {
	this.docDefID = docDefID;
}
public String getBizID() {
	return bizID;
}
public void setBizID(String bizID) {
	this.bizID = bizID;
}
public String getDocDefName() {
	return docDefName;
}
public void setDocDefName(String docDefName) {
	this.docDefName = docDefName;
}
public int getDispOrder() {
	return dispOrder;
}
public void setDispOrder(int dispOrder) {
	this.dispOrder = dispOrder;
}

}
